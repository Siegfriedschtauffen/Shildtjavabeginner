package avtoupakovka;

// Применение автоупаковки и автораспаковки.
public class AutoBox {
    public static void main(String[] args) {

        // Автоустановка и автораспаковка значения 100.
        Integer iOb = 100;
        int i = iOb;

        System.out.println(i + " " + iOb); // displays 100 100
    }
}
